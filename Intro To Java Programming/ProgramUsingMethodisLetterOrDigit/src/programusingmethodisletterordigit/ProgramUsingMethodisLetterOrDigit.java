package programusingmethodisletterordigit;

public class ProgramUsingMethodisLetterOrDigit {

    public static void main(String[] args) {
        char value1 = 'U';
        char value2 = '9';
        char value3 = 'w';
        char value4 = '*';
        System.out.println(Character.isLetterOrDigit(value1));
        System.out.println(Character.isLetterOrDigit(value2));
        System.out.println(Character.isLetterOrDigit(value3));
        System.out.println(Character.isLetterOrDigit(value4));
    }
}
